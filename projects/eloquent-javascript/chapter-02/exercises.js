function drawChessboard(number) {
let board = "";
for (let i = 0; i < number; i++) {
  for (let j = 0; j < number; j++) {//nested for loop
    if ((i + j) % 2 == 0) {
      board += " ";
    } else {
      board += "#";
    }
  }
  board += "\n";
}
console.log(board);
}
